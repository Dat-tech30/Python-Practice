print(type(2.11))                # Float
print(type(2 + 4j))              # Complex
print(type('Dat'))               # String
print(type([1, 2, 3]))           # List
print(type({'name':'Dat'}))      # Dictionary
print(type({9.2, 2.11, 2.5}))    # Set
print(type((6.8, 2.11, 2.7)))    # Tuple